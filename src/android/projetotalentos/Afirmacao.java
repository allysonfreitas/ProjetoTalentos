package android.projetotalentos;


public class Afirmacao {
	int id;
	String descricao;
		
	public Afirmacao() {
		
		
	}
	
	/**
	 * Set e Get id
	 */
	public int getId() {
		return this.id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	/**
	 * Set e Get descricao
	 */
	public String getDescricao() {
		return this.descricao;
	}
	
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	
	
}
